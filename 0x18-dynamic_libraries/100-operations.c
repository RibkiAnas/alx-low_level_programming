/**
* add - function that adds two numbers
* @a: number one
* @b: number two
*
* Return: result
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - function that sub two numbers
* @a: number one
* @b: number two
*
* Return: result
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - function that mul two numbers
* @a: number one
* @b: number two
*
* Return: result
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - function that div two numbers
* @a: number one
* @b: number two
*
* Return: result
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - function that mod two numbers
* @a: number one
* @b: number two
*
* Return: result
*/
int mod(int a, int b)
{
	return (a % b);
}
